import configparser
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def main() -> int:
    config_path = ROOT / "app_config.ini"
    if not config_path.exists():
        raise SystemExit(f"Missing {config_path}")

    cp = configparser.ConfigParser()
    cp.read(config_path, encoding="utf-8")

    app_name = cp.get("APP", "name", fallback="PersonalAiAgentSurya").strip()
    exe_base = cp.get("BUILD", "exe_base_name", fallback=app_name).strip()
    publisher = cp.get("BUILD", "publisher", fallback=app_name).strip()
    version = cp.get("BUILD", "version", fallback="1.0.0").strip()
    guid = cp.get("BUILD", "app_id_guid", fallback="7B3AAB64-1E4D-4A5A-A2D8-7D3F80EFC9B1").strip()

    out = ROOT / "installer" / "app_config.issinc"
    out.write_text(
        "\n".join(
            [
                "; Auto-generated by build/sync_inno_config.py from app_config.ini",
                f'#define MyAppName "{app_name}"',
                f'#define MyAppExeName "{exe_base}.exe"',
                f'#define MyAppPublisher "{publisher}"',
                f'#define MyAppVersion "{version}"',
                f'#define MyAppIdGuid "{guid}"',
                "",
            ]
        ),
        encoding="utf-8",
    )

    print(f"Wrote {out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
